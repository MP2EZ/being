#!/usr/bin/env node
/**
 * Generates app/src/features/profile/content/legalContent.generated.ts from
 * the canonical legal documents at docs/legal/*.md (outside app/).
 *
 * Why this exists: Metro's release-mode bundler refuses to resolve module paths
 * that traverse above the project root (app/), so the previous approach of
 * `require('../../../../../docs/legal/privacy-policy.md')` worked in dev (where
 * Metro serves files from disk on demand) but broke any `expo export` or
 * `--configuration Release` build. Moving the import target into app/ as a
 * generated TS module sidesteps Metro path resolution entirely.
 *
 * Wired into:
 *   - postinstall (runs after every `npm install`, in `app/`)
 *   - prestart / preios / preandroid (catches edits to docs/legal/ between installs)
 *
 * Canonical source: docs/legal/*.md at the worktree root. Edit those, not the
 * generated file. The generated file is gitignored.
 */

const fs = require('fs');
const path = require('path');

const SOURCES = [
  { key: 'privacyPolicyContent', file: 'privacy-policy.md' },
  { key: 'termsOfServiceContent', file: 'terms-of-service.md' },
  { key: 'medicalDisclaimerContent', file: 'medical-disclaimer.md' },
  { key: 'californiaPrivacyContent', file: 'california-privacy.md' },
  { key: 'multiStatePrivacyContent', file: 'multi-state-privacy.md' },
  { key: 'supportContent', file: 'support.md' },
];

const APP_DIR = path.resolve(__dirname, '..');
const REPO_ROOT = path.resolve(APP_DIR, '..');
const DOCS_LEGAL = path.join(REPO_ROOT, 'docs', 'legal');
const OUT_PATH = path.join(APP_DIR, 'src', 'features', 'profile', 'content', 'legalContent.generated.ts');

function escapeForTemplateLiteral(raw) {
  return raw.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$\{/g, '\\${');
}

function readSource({ file }) {
  const src = path.join(DOCS_LEGAL, file);
  if (!fs.existsSync(src)) {
    throw new Error(`Missing legal source: ${src}`);
  }
  return fs.readFileSync(src, 'utf8');
}

function main() {
  const exports = SOURCES.map(({ key, file }) => {
    const raw = readSource({ file });
    return `export const ${key}: string = \`${escapeForTemplateLiteral(raw)}\`;\n`;
  }).join('\n');

  const header =
    '// AUTO-GENERATED by scripts/generate-legal-content.js — do not edit.\n' +
    '// Canonical source: docs/legal/*.md (worktree root).\n' +
    '// Regenerate via `node scripts/generate-legal-content.js` (also runs on postinstall).\n\n' +
    '/* eslint-disable */\n\n';

  const next = header + exports;

  if (fs.existsSync(OUT_PATH) && fs.readFileSync(OUT_PATH, 'utf8') === next) {
    return;
  }

  fs.mkdirSync(path.dirname(OUT_PATH), { recursive: true });
  fs.writeFileSync(OUT_PATH, next);
  console.log(`generated ${path.relative(APP_DIR, OUT_PATH)} (${SOURCES.length} docs)`);
}

main();
